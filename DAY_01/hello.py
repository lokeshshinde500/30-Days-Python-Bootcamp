# PYTHON DATA TYPES
# 1. String
# 2. Integer
# 3. Float
# 4. Boolean
# 5. List
# 6. Tuple
# 7. Set
# 8. Dictionary


# 1. String
name = "Anurag"
print(name)
print(type(name))

# 2. Integer
age = 10
print(age)
print(type(age))

# 3. Float
pi = 3.14
print(pi)
print(type(pi))

# 4. Boolean
isVerified = True
print(isVerified)
print(type(isVerified))


# 5. List
users = ["Lokesh" , "Jayesh" , "Nikhil", "Nikhil"]
print(users)
print(type(users))

# 6. Tuple
fruits = ("Mango", "Apple", "Cherry")
print(fruits)
print(type(fruits))

# 7. Set
numbers = {1,4,5,6,65,6}
print(numbers)
print(type(numbers))

# 8. Dictionary
userData = {
  "name": "Lokesh",
  "age": 24,
  "role": "Developer"
}
print(userData)
print(type(userData))


# type conversion

# 1. str()
# 2. int()
# 3. float()
# 4. bool()
# 5. list()
# 6. tuple()
# 7. set()
# 8. dict()


# 1. str()
age = 10
print(type(age))
age = str(age)
print(type(age))

# 2. int()
age = "10"
print(type(age))
age = int(age)
print(type(age))

# 3. float()
age = 10
print(type(age))
age = float(age)
print(type(age))


# 4. bool()
age = 10
print(type(age))
age = bool(age)
print(type(age))


